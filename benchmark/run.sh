#!/usr/bin/env bash
# Laconic benchmark.
# Runs each prompt N times via `claude -p` in two modes and reports
# median output tokens (lower = better):
#   Off → LACONIC_BENCH=1 (SessionStart hook skips → no rule in prompt)
#   On  → --append-system-prompt with the laconic rule
# This isolates the rule's effect from any hook side-effects, so the
# comparison is apples-to-apples.
#
# Requirements:
#   - claude CLI on PATH
#   - jq
#
# Env:
#   RUNS=3   number of runs per prompt per mode (default 3)

set -euo pipefail

cd "$(dirname "$0")"
PROMPTS="prompts.txt"
OUT="results.md"
RUNS="${RUNS:-3}"

if ! command -v claude >/dev/null; then echo "claude CLI not found"; exit 1; fi
if ! command -v jq >/dev/null; then echo "jq not found"; exit 1; fi

LACONIC_RULE='LACONIC MODE. Drop articles/filler/hedging. Fragments OK. Hard cap <=20 words or <=3 lines. First line = answer. No preamble/summary/next-steps. No code blocks in chat. Read narrowly: prefer Grep/Glob + Read offset/limit over reading whole files. Commits/security: normal prose.'

run_off() {
  local prompt="$1"
  LACONIC_BENCH=1 claude -p "$prompt" --output-format json 2>/dev/null \
    | jq -r '.usage.output_tokens // empty'
}

run_on() {
  local prompt="$1"
  LACONIC_BENCH=1 claude -p "$prompt" \
    --append-system-prompt "$LACONIC_RULE" \
    --output-format json 2>/dev/null \
    | jq -r '.usage.output_tokens // empty'
}

# Reads stdin: one integer per line. Prints median (ties → lower middle).
median() {
  sort -n | awk '{a[NR]=$1} END{ if(NR==0){print 0; exit} print a[int((NR+1)/2)] }'
}

run_n() {
  local fn="$1" prompt="$2"
  local i v
  for i in $(seq 1 "$RUNS"); do
    v=$("$fn" "$prompt"); echo "${v:-0}"
  done | median
}

{
  echo "# Laconic benchmark results"
  echo
  echo "Median output tokens over **$RUNS runs** per prompt per mode. Lower = better."
  echo
  echo "| # | Prompt | Off | On | Saved |"
  echo "| - | ------ | --- | -- | ----- |"
} > "$OUT"

i=0
total_off=0
total_on=0
while IFS= read -r prompt; do
  [ -z "$prompt" ] && continue
  i=$((i+1))
  echo "[$i] $prompt" >&2

  off=$(run_n run_off "$prompt")
  on=$(run_n run_on "$prompt")

  off=${off:-0}; on=${on:-0}
  if [ "$off" -gt 0 ]; then
    saved=$(( (off - on) * 100 / off ))
  else
    saved=0
  fi
  total_off=$((total_off + off))
  total_on=$((total_on + on))

  short=$(echo "$prompt" | cut -c1-60)
  echo "| $i | $short | $off | $on | ${saved}% |" >> "$OUT"
done < "$PROMPTS"

if [ "$total_off" -gt 0 ]; then
  total_saved=$(( (total_off - total_on) * 100 / total_off ))
else
  total_saved=0
fi

{
  echo "| | **Total (median sum)** | **$total_off** | **$total_on** | **${total_saved}%** |"
  echo
  echo "_Generated by \`benchmark/run.sh\` with RUNS=$RUNS. Reproduce: \`RUNS=$RUNS ./benchmark/run.sh\`._"
} >> "$OUT"

echo "Wrote $OUT"
