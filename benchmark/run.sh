#!/usr/bin/env bash
# Laconic benchmark.
# Runs each prompt twice via `claude -p`: once normal, once with /laconic.
# Counts output tokens via `claude --output-format json` (.usage.output_tokens).
# Writes results.md with a comparison table.
#
# Requirements:
#   - claude CLI on PATH
#   - jq
#   - laconic plugin installed and enabled

set -euo pipefail

cd "$(dirname "$0")"
PROMPTS="prompts.txt"
OUT="results.md"

if ! command -v claude >/dev/null; then echo "claude CLI not found"; exit 1; fi
if ! command -v jq >/dev/null; then echo "jq not found"; exit 1; fi

run_once() {
  local prompt="$1"
  claude -p "$prompt" --output-format json 2>/dev/null \
    | jq -r '.usage.output_tokens // empty'
}

{
  echo "# Laconic benchmark results"
  echo
  echo "Output tokens per prompt. Lower = better."
  echo
  echo "| # | Prompt | Off | On | Saved |"
  echo "| - | ------ | --- | -- | ----- |"
} > "$OUT"

i=0
total_off=0
total_on=0
while IFS= read -r prompt; do
  [ -z "$prompt" ] && continue
  i=$((i+1))
  echo "[$i] $prompt" >&2

  off=$(run_once "$prompt")
  on=$(run_once "/laconic $prompt")

  off=${off:-0}; on=${on:-0}
  if [ "$off" -gt 0 ]; then
    saved=$(( (off - on) * 100 / off ))
  else
    saved=0
  fi
  total_off=$((total_off + off))
  total_on=$((total_on + on))

  short=$(echo "$prompt" | cut -c1-60)
  echo "| $i | $short | $off | $on | ${saved}% |" >> "$OUT"
done < "$PROMPTS"

if [ "$total_off" -gt 0 ]; then
  total_saved=$(( (total_off - total_on) * 100 / total_off ))
else
  total_saved=0
fi

{
  echo "| | **Total** | **$total_off** | **$total_on** | **${total_saved}%** |"
  echo
  echo "_Generated by \`benchmark/run.sh\`. Reproduce: \`./benchmark/run.sh\`._"
} >> "$OUT"

echo "Wrote $OUT"
